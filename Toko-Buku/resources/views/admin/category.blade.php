    <script>
        document.addEventListener('DOMContentLoaded', () => {
            const editButtons = document.querySelectorAll('.edit-btn');
            const form = document.getElementById('categoryForm');

            editButtons.forEach(button => {
                button.addEventListener('click', (event) => {
                    event.preventDefault(); // Hindari reload halaman

                    const category_id = button.dataset.id;

                    // Update action URL dan method untuk edit
                    form.action = `/admin/categories/${category_id}`;
                    form.querySelector('input[name="_method"]').value = 'PUT'; // Laravel PUT method

                    // Isi form dengan data
                    form.id.value = category_id;
                    form.name.value = button.dataset.name || ''; // Hindari "undefined"
                    form.description.value = button.dataset.description || '';
                });
            });
        });
    </script>
    <script>
        if (document.getElementById("search-table") && typeof simpleDatatables.DataTable !== 'undefined') {
